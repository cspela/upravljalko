    public static void resetirajTabelo(String iskalniNiz) throws ClassNotFoundException, SQLException{
        Delavec del = new Delavec(); 
        if(jTable1_seznam_delavcev != null){
            jTable1_seznam_delavcev.setModel(new DefaultTableModel(null, new Object[]{"Id", "Ime", "Priimek"}));
            del.izpisDelavcev(jTable1_seznam_delavcev, iskalniNiz);
        }
    }
           
    private void getDataTable(){
        int rowIndex = jTable1_seznam_delavcev.getSelectedRow(); 
        DefaultTableModel model = (DefaultTableModel)jTable1_seznam_delavcev.getModel();
        jTextField_id.setText(model.getValueAt(rowIndex, 0).toString()); 
        jTextField_ime.setText(model.getValueAt(rowIndex, 1).toString()); 
        jTextField_priimek.setText(model.getValueAt(rowIndex, 2).toString()); 
    }
    
    private boolean verifyText(){
        if(jTextField_ime.getText().equals("") || jTextField_priimek.getText().equals("") || jTextField_id.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Eno ali več polij je praznih!");
            return false; 
        }else{
           return true; 
        }
    }
       
