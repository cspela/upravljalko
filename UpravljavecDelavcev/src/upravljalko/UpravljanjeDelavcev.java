    private void posodobiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_posodobiActionPerformed
        if(verifyText()){
            int id = Integer.valueOf(jTextField_id.getText());
            String ime = jTextField_ime.getText();
            String priimek = jTextField_priimek.getText();
            try {
                if(!del.obstojDelavca(ime, priimek)){
                    del.dodajPosodobiBrisiDelavca('u', ime, priimek, id);
                    //Resetiraj tabele in comboboxe
                    resetirajTabelo("");
                    MainForm.resetirajTabelo("");
                    IzbrisiPovezavo.resetComboDelavec();
                    IzbrisiPovezavo.resetComboSkupina();
                    IzbrisiPovezavo.resetirajTabelo("");
                    DodajPovezavo.resetComboDelavec();
                    DodajPovezavo.resetComboSkupina();
                }else{
                    if(del.spremembe(ime, priimek, id)){
                        JOptionPane.showMessageDialog(null, "Ni bilo vnesenih sprememb");
                    }else{
                        JOptionPane.showMessageDialog(null, "Delavec s takšnim imenom in priimkom že obstaja!");
                    }
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(UpravljanjeDelavcev.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_posodobiActionPerformed

    private void izbrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_izbrisiActionPerformed
        if(jTextField_id.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Delavec ni izbran.");
        }else{
            try {
                int id = Integer.valueOf(jTextField_id.getText());
                del.dodajPosodobiBrisiDelavca('d', null, null, id);
                //Resetiraj tabele in comboboxe
                resetirajTabelo("");
                MainForm.resetirajTabelo("");
                IzbrisiPovezavo.resetComboDelavec();
                IzbrisiPovezavo.resetComboSkupina();
                IzbrisiPovezavo.resetirajTabelo("");
                DodajPovezavo.resetComboDelavec();
                DodajPovezavo.resetComboSkupina();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(UpravljanjeDelavcev.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_izbrisiActionPerformed

